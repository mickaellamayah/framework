package mg.tool;

public class VerbMethod {

    private String methodName;
    private String verb;


    public VerbMethod(){}
    
    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }

    public String getVerb() {
        return verb;
    }

    public void setVerb(String verb) {
        this.verb = verb;
    }
}
